    public void cargarTareas() {
        String consulta = "FROM Tarea";
        List<Tarea> departamentos = hi.lanzarConsulta(consulta);
       
        borrarFilas();
        if (!departamentos.isEmpty()) {
            for (Tarea a : departamentos) {
                //Recuperamos los campos que queremos que se muestren en la tabla
                Integer Id = a.getIdTarea();
                String descripcion = a.getDescripcion();
                String responsable = a.getResponsable();
                String estado= a.getEstado();

                modeloTabla.addRow(new Object[]{
                    Id,descripcion, responsable,estado
                });

                jtTareas.setModel(modeloTabla);

            }

        } else {

            JOptionPane.showMessageDialog(null, "No existen datos");
        }
       
    }
    
    public void iniciarTabla() {
        //Reajustamos el modelo la tabla para que definir que tipo de dato da cada columna
        //Con esto conseguimos que funcione correctamente la reordenaci√≥n de filas por columna
        modeloTabla = new DefaultTableModel() {
            Class[] tiposDeColumna = new Class[]{
                java.lang.Integer.class, java.lang.String.class,java.lang.String.class, java.lang.String.class

            };

            @Override
            public Class getColumnClass(int indiceColumna) {
                return tiposDeColumna[indiceColumna];
            }
        };
        //Damos la posibilidad a la tabla de reordenarse en funcion de la columna
        //cuando el usuario pulsa sobre la cabecera
        jtTareas.setAutoCreateRowSorter(true);

        //Cabeceras de las columnas, mostraremos solamente el titulo, duracion, calificacion y extras.
        modeloTabla.addColumn("Id Tarea");
        modeloTabla.addColumn("Responsable");
        modeloTabla.addColumn("Descripcion");
        modeloTabla.addColumn("Estado");


        jtTareas.setModel(modeloTabla);

   
    }


    private void jtGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtGuardarActionPerformed
        if (jtDescripcion.getText().length()==0 ||
            jtResponsable.getText().length()==0  )
        {
            JOptionPane.showMessageDialog(null, "No han sido rellenados todos los campos");
        }
        else
        {
            
             int id=Integer.parseInt(jtID.getText());
        String descripcion=jtDescripcion.getText();
        String responsable =jtResponsable.getText();
        String estado=jtEstado.getSelectedItem().toString();
        //public Tarea(Integer idTarea, String descripcion, String responsable, String estado)
        Tarea a1=new Tarea(id,descripcion,responsable,estado);
             
           if (hi.insertar(a1))
            {
                JOptionPane.showMessageDialog(null, "Se ha registrado el alta correctamente");
            }
           else
              
              JOptionPane.showMessageDialog(null, "No se han introducido los datos correctamente"); 
            
            
        }
        
        
    }//GEN-LAST:event_jtGuardarActionPerformed

